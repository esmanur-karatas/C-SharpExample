//MANTIKSAL OPERATÖRLER: Ve( && ), veya( || )

// VEYA ||
// BURADA EKRANA 1 VEYA 1 İKİSİNDEN BİRİNİ YAZDIRACAK
using System;
using System.ComponentModel.Design;

Console.WriteLine(1 | 1);

// burada da yukarıdakinin aynısını yaptık.
Console.WriteLine(2 | 1);

//burada da boolean değeri seçtik true veya false bastırır.
Console.WriteLine(false | true);

//VE &&
Console.WriteLine(0 & 1); //O ve 1 i ekrana bastırdık. birini bastırdı.
